export default defineComponent({
  name: "Basic",
  components: {
    TheBanner,
    Card,
  },
  data() {
    return {
      imageSrc: "../../assets/img/homepage/maamoul4.jpeg",
      isXs: false,
      isSm: false,
      isMd: false,
      is2xl: false,
    };
  },
  mounted() {
    // Add event listener to check screen size on mount
    this.checkScreenSize();

    // Add event listener to dynamically update screen size
    window.addEventListener("resize", this.checkScreenSize);
  },
  beforeUnmount() {
    // Remove event listener on component unmount
    window.removeEventListener("resize", this.checkScreenSize);
  },
  methods: {
    checkScreenSize() {
      this.isMd = window.matchMedia(
        "(min-width: 768px) and (max-width: 1023px)"
      ).matches;
      this.is2xl = window.matchMedia("(min-width: 1536px)").matches;
      this.isSm = window.matchMedia("(max-width: 767px)").matches;
      this.isXs = window.matchMedia("(max-width: 639px)").matches;

      // Update image source based on screen size
      if (this.isMd || this.isSm || this.isXs) {
        this.imageSrc = maamoul;
      } else if (this.is2xl) {
        this.imageSrc = maamoul4;
      } else {
        // Default image source for other screen sizes
        this.imageSrc = maamoul4;
      }
    },
  },
});
